package jnhouse.topwellsoft.com.jnhouse_android.util;

import net.tsz.afinal.FinalBitmap;
import android.content.Context;
import android.widget.ImageView;

import jnhouse.topwellsoft.com.jnhouse_android.R;
import jnhouse.topwellsoft.com.jnhouse_android.constant.JnHouse_Record;

public class FinalBitmapUtil {

	public static void finalBitmap(Context context, ImageView imageView, String picPath){
		
		FinalBitmap finalBitmap = FinalBitmap.create(context);
		finalBitmap.configLoadingImage(R.drawable.wutouxiang);
		finalBitmap.configLoadfailImage(R.drawable.wutouxiang);
		finalBitmap.display(imageView, JnHouse_Record.Key_user_avatar);
		
	}
	
	public static void finalBitmapPic(Context context, ImageView imageView, String picPath){
		
		FinalBitmap finalBitmap = FinalBitmap.create(context);
		finalBitmap.configLoadingImage(R.drawable.wutouxiang);
		finalBitmap.configLoadfailImage(R.drawable.wutouxiang);
		finalBitmap.display(imageView, JnHouse_Record.Key_user_avatar);
	}
}
