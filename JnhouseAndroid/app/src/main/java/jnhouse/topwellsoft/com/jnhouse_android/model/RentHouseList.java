package jnhouse.topwellsoft.com.jnhouse_android.model;

import java.io.Serializable;
import java.util.List;

/**
 * Created by chenchen on 2016/6/24.
 */
public class RentHouseList implements Serializable {
    private List<RentHouseFeature> feature_list;
    private List<RentingHouseEntity> data_list;
    private String data_size;

    public List<RentHouseFeature> getFeature_list() {
        return feature_list;
    }

    public void setFeature_list(List<RentHouseFeature> feature_list) {
        this.feature_list = feature_list;
    }

    public List<RentingHouseEntity> getData_list() {
        return data_list;
    }

    public void setData_list(List<RentingHouseEntity> data_list) {
        this.data_list = data_list;
    }

    public String getData_size() {
        return data_size;
    }

    public void setData_size(String data_size) {
        this.data_size = data_size;
    }
}
