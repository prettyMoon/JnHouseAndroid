public class EventCommunityUtil {
    private  String msg;
    private String city;
    private String cityarea;
    private String cityarea2;
    private String bor_id;


    public EventCommunityUtil(String bor_id,String msg,String city,String cityarea,String cityarea2) {
        this.bor_id = bor_id;
        this.msg = msg;
        this.city = city;
        this.cityarea = cityarea;
        this.cityarea2 = cityarea2;

    }

    public String getBor_id() {
        return bor_id;
    }

    public String getMsg() {
        return msg;
    }

    public String getCity() {
        return city;
    }

    public String getCityarea() {
        return cityarea;
    }

    public String getCityarea2() {
        return cityarea2;
    }
}
