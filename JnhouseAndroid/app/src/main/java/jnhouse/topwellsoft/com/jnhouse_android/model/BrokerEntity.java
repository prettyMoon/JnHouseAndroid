package jnhouse.topwellsoft.com.jnhouse_android.model;

import java.io.Serializable;

/**
 * Created by topwellsoft on 2016/8/23.
 */
public class BrokerEntity implements Serializable {
    private String broker_id;
    private String broker_name;
    private String broker_avar;

    public String getBroker_id() {
        return broker_id;
    }

    public void setBroker_id(String broker_id) {
        this.broker_id = broker_id;
    }

    public String getBroker_name() {
        return broker_name;
    }

    public void setBroker_name(String broker_name) {
        this.broker_name = broker_name;
    }

    public String getBroker_avar() {
        return broker_avar;
    }

    public void setBroker_avar(String broker_avar) {
        this.broker_avar = broker_avar;
    }
}
