package jnhouse.topwellsoft.com.jnhouse_android.model;

import java.io.Serializable;

/**
 * Created by admin on 2016/6/6.
 */
public class LoginEntity implements Serializable {

    private int code;//0:登录成功 -1：登录异常 100:用户名密码错误
    private String execCode;
    private String userUUID;//一个UUID标识（登录成功时，需存储）
    private String user_id;//用户id
    private String username;//用户账号
    private String realname;//昵称（可空）
    private String avatar = "";//头像地址（可空）
    private String hx_pwd;
    private String password;

    public void setHx_pwd(String hx_pwd) {
        this.hx_pwd = hx_pwd;
    }

    public String getHx_pwd() {
        return hx_pwd;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getExecCode() {
        return execCode;
    }

    public void setExecCode(String execCode) {
        this.execCode = execCode;
    }

    public String getUserUUID() {
        return userUUID;
    }

    public void setUserUUID(String userUUID) {
        this.userUUID = userUUID;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRealname() {
        return realname;
    }

    public void setRealname(String realname) {
        this.realname = realname;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
