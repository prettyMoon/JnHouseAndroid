public class Map_NewHouse_Detil implements Serializable {
  private String  borough_thumb;//标题图
  private  String borough_name;//新盘名称
   private String borough_avgprice;//均价（为0时显示价格待定）
   private String borough_address;//楼盘地址
  private  String  house_feature;//特色（带，字符串，需分割）

    public String getBorough_thumb() {
        return borough_thumb;
    }

    public void setBorough_thumb(String borough_thumb) {
        this.borough_thumb = borough_thumb;
    }

    public String getBorough_name() {
        return borough_name;
    }

    public void setBorough_name(String borough_name) {
        this.borough_name = borough_name;
    }

    public String getBorough_avgprice() {
        return borough_avgprice;
    }

    public void setBorough_avgprice(String borough_avgprice) {
        this.borough_avgprice = borough_avgprice;
    }

    public String getBorough_address() {
        return borough_address;
    }

    public void setBorough_address(String borough_address) {
        this.borough_address = borough_address;
    }

    public String getHouse_feature() {
        return house_feature;
    }

    public void setHouse_feature(String house_feature) {
        this.house_feature = house_feature;
    }
}
